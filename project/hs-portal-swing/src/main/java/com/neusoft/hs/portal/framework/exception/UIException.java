package com.neusoft.hs.portal.framework.exception;

import com.neusoft.hs.platform.exception.HsException;

public class UIException extends HsException {

	public UIException() {
		super();
	}

	public UIException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public UIException(String arg0) {
		super(arg0);
	}

	public UIException(Throwable arg0) {
		super(arg0);
	}

}
